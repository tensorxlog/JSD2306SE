package string;

public class ValueOfDemo {
    public static void main(String[] args) {
        int a = 5;
        double b = 12.03;
        float c = 3.54f;

        System.out.println(String.valueOf(a));
        System.out.println(String.valueOf(b));
        System.out.println(String.valueOf(c));
    }
}
